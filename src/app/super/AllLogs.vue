<template>
  <section class="section">
    <back-button url="/s" />
    <h2>All Logs</h2>
    <table>
      <tr v-for="log in logs" :key="log.id">
        <td class="pb-2">
          {{ log.cmd }}
          <div class="ml-2">{{ log.key }} {{ log.value }}</div>
        </td>
        <td class="pl-2">{{ log.success ? "success" : log.error }}</td>
        <td class="pl-2">
          <router-link :to="`/s/admins/${log.uid}`">{{
            log.email || log.uid
          }}</router-link>
        </td>
        <td class="pl-2">{{ log.uidSuper.slice(0, 8) + "..." }}</td>
      </tr>
    </table>
  </section>
</template>

<script>
import BackButton from "@/components/BackButton";
import { db } from "@/plugins/firebase";

export default {
  metaInfo() {
    return {
      title: [this.defaultTitle, "Super All Log"].join(" / "),
    };
  },
  components: {
    BackButton,
  },
  data() {
    return {
      logs: [],
      detacher: null,
    };
  },
  async mounted() {
    if (!this.$store.state.user || this.$store.getters.isNotSuperAdmin) {
      this.$router.push("/");
    }
  },
  created() {
    this.detatcher = db
      .collectionGroup("adminlogs")
      .orderBy("createdAt", "desc")
      .limit(100)
      .onSnapshot((snapshot) => {
        this.logs = snapshot.docs.map((doc) => {
          const log = doc.data();
          log.id = doc.id;
          log.createdAt = log.createdAt.toDate();
          return log;
        });
      });
  },
  destroyed() {
    this.detatcher && this.detatcher();
  },
};
</script>
