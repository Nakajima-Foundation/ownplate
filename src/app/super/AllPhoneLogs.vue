<template>
  <section class="mx-auto max-w-full px-6 pb-12 pt-4">
    <back-button url="/s" />
    <h2>All Logs</h2>
    <table>
      <tr v-for="log in logs" :key="log.id">
        <td class="pb-2">
          {{
            moment(log.updatedAt.toDate()).format("YYYY:MM:DD HH:mm")
          }}/<router-link :to="`/r/${log.restaurantId}`">{{
            log.restaurantId
          }}</router-link
          >/{{ log.orderId }}
        </td>
      </tr>
    </table>
  </section>
</template>

<script>
import BackButton from "@/components/BackButton";
import { db } from "@/plugins/firebase";

export default {
  metaInfo() {
    return {
      title: [this.defaultTitle, "Super All Phone Logs"].join(" / "),
    };
  },
  components: {
    BackButton,
  },
  data() {
    return {
      logs: [],
      detacher: null,
    };
  },
  async mounted() {
    if (!this.$store.state.user || this.$store.getters.isNotSuperAdmin) {
      this.$router.push("/");
    }
  },
  created() {
    this.detatcher = db
      .collectionGroup("phoneLog")
      .orderBy("updatedAt", "desc")
      .limit(100)
      .onSnapshot((snapshot) => {
        this.logs = snapshot.docs.map((doc) => {
          const log = doc.data();
          log.id = doc.id;
          log.createdAt = log.updatedAt.toDate();
          return log;
        });
        console.log(this.logs);
      });
  },
  destroyed() {
    this.detatcher && this.detatcher();
  },
};
</script>
