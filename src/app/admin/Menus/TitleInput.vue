<template>
  <div>
    <!-- Title Card -->
    <div class="bg-form r-8 p-l-16 p-r-16 p-t-16 p-b-16 m-t-24">
      <b-field>
        <b-input
          ref="textInput"
          v-model="title.name"
          @blur="blur"
          :placeholder="$t('editTitle.enterCategory')"
        ></b-input>
      </b-field>
    </div>

    <!-- Card Actions -->
    <div class="m-t-8 p-b-8 p-l-8 p-r-8">
      <div class="cols">
        <div class="flex-1">
          <!-- Position Up -->
          <b-button
            class="b-reset op-button-pill h-36 bg-form m-r-8"
            v-if="position !== 'first'"
            @click="positionUp"
          >
            <i class="material-icons c-primary s-18 p-l-8 p-r-8"
              >arrow_upward</i
            >
          </b-button>
          <!-- Disable if First -->
          <b-button
            class="b-reset op-button-pill h-36 bg-form m-r-8"
            disabled
            v-else
          >
            <i class="material-icons c-text-black-disabled s-18 p-l-8 p-r-8"
              >arrow_upward</i
            >
          </b-button>

          <!-- Position Down -->
          <b-button
            class="b-reset op-button-pill h-36 bg-form m-r-8"
            v-if="position !== 'last'"
            @click="positionDown"
          >
            <i class="material-icons c-primary s-18 p-l-8 p-r-8"
              >arrow_downward</i
            >
          </b-button>
          <!-- Disable if Last -->
          <b-button
            class="b-reset op-button-pill h-36 bg-form m-r-8"
            disabled
            v-else
          >
            <i class="material-icons c-text-black-disabled s-18 p-l-8 p-r-8"
              >arrow_downward</i
            >
          </b-button>

          <!-- Duplicate -->
          <b-button
            class="b-reset op-button-pill h-36 bg-form m-r-8"
            @click="forkItem"
          >
            <i class="material-icons c-primary s-18 p-l-8 p-r-8">queue</i>
          </b-button>
        </div>
        <div>
          <b-button
            class="b-reset op-button-pill h-36 bg-status-red-bg"
            disabled
          >
            <i class="material-icons c-status-red s-18 p-l-8 p-r-8">delete</i>
          </b-button>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import Vue from "vue";
import { db, storage } from "~/plugins/firebase.js";

export default {
  name: "TitleInput",
  props: {
    title: {
      type: Object,
      required: true
    },
    position: {
      type: String,
      required: true
    }
  },
  created() {
    this.checkAdminPermission();
  },
  mounted() {
    this.$refs.textInput.focus();
  },
  methods: {
    blur() {
      // save and update this.
      this.$emit("updateTitle", this.title);
    },
    positionUp() {
      this.$emit("positionUp", this.title.id);
    },
    positionDown() {
      this.$emit("positionDown", this.title.id);
    },
    forkItem() {
      this.$emit("forkItem", this.title.id);
    }
  }
};
</script>
